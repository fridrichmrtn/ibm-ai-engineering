def area_of_circle(radius):
    """Calculate the area of a circle given its radius."""
    import math
    return math.pi * (radius ** 2)

def perimeter_of_circle(radius):
    """Calculate the perimeter (circumference) of a circle given its radius."""
    import math
    return 2 * math.pi * radius

def area_of_rectangle(length, width):
    """Calculate the area of a rectangle given its length and width."""
    return length * width

def perimeter_of_rectangle(length, width):
    """Calculate the perimeter of a rectangle given its length and width."""
    return 2 * (length + width)

def area_of_triangle(base, height):
    """Calculate the area of a triangle given its base and height."""
    return 0.5 * base * height

def perimeter_of_triangle(a, b, c):
    """Calculate the perimeter of a triangle given the lengths of its three sides."""
    return a + b + c

